/**
 * Helpers used to calculate time in milliseconds
 */
var second 	= exports.SECOND 	= 1000;
var minute 	= exports.MINUTE 	= 60 * second;
var hour 		= exports.HOUR 		= 60 * minute;
var day 		= exports.DAY			= 24 * hour;
var week		= exports.WEEK		= 7 * day;
var month		= exports.MONTH		= 30 * day;
var year		= exports.YEAR		= 365 * day;